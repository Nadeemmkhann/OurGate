class User {
  constructor(
    firstName,
    lastName,
    wing,
    flat,
    address,
    email,
    password,
    phoneNumber,
    gender
  ) {
    this.firstName = firstName;
    this.lastName = lastName;
    this.wing = wing;
    this.flat = flat;
    this.address = address;
    this.email = email;
    this.password = password;
    this.phoneNumber = phoneNumber;
    this.gender = gender;
  }
}
